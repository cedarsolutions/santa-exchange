package com.cedarsolutions.santa.shared.domain.user;

import com.google.gwt.view.client.ProvidesKey;

/**
 * Key provider for RegisteredUser.
 * @author Kenneth J. Pronovici <pronovic@ieee.org>
 */
public class RegisteredUserKeyProvider implements ProvidesKey<RegisteredUser> {
    @Override
    public Object getKey(RegisteredUser item) {
        return item == null ? null : item.getUserId();
    }
}
