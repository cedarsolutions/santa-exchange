package com.cedarsolutions.santa.shared.domain.audit;

/**
 * Keys used in audit events when storing extra data.
 * @author Kenneth J. Pronovici <pronovic@ieee.org>
 */
public enum ExtraDataKey {

    MODULE,         // The module that was involved
    USER_ID,        // The id of the user that was operated on
    EMAIL_ADDRESS,  // The email address of the user that was operated on
    EXCHANGE_ID,    // The id of the exchange that was operated on
    RECIPIENTS;     // The number of recipients that emails were sent to

}
