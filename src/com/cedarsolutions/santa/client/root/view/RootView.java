package com.cedarsolutions.santa.client.root.view;

import com.google.gwt.core.client.GWT;
import com.google.gwt.uibinder.client.UiBinder;
import com.google.gwt.user.client.ui.Composite;
import com.google.gwt.user.client.ui.Widget;

/**
 * Empty view that provides a startView target for the root event bus.
 * @author Kenneth J. Pronovici <pronovic@ieee.org>
 */
public class RootView extends Composite implements IRootView {

    /** Reference to the UI binder. */
    private static ViewUiBinder BINDER = GWT.create(ViewUiBinder.class);

    /** UI binder interface. */
    protected interface ViewUiBinder extends UiBinder<Widget, RootView> {
    }

    /** Create the view. */
    public RootView() {
        this.initWidget(BINDER.createAndBindUi(this));
    }

}
